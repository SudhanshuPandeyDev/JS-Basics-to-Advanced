// Logical Operators in JavaScript: AND, OR, and NOT

// && -> and
// || -> or
// ! -> reverse

let isLoggedin = true;
let isPaid = true;
console.log(isLoggedin && isPaid);

let isEmailuser = true;
let isGoogleuser = false;
console.log(isEmailuser || isGoogleuser);

let isRaining = true;
console.log(isRaining);
let isNotRaining = !isRaining;
console.log(isNotRaining);
